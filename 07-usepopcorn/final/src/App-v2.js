/**
 * Calculates the average of an array of numbers.
 * @param {number[]} arr - Array of numbers.
 * @returns {number} The average of the numbers in the array.
 */
const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

